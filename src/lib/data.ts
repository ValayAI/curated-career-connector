export const getJobById = (id: string): Job | undefined => {
  return jobs.find(job => job.id === id);
};

export const filterJobs = (filters: Partial<Record<string, any>>): Job[] => {
  return jobs.filter(job => {
    let matches = true;
    
    Object.entries(filters).forEach(([key, value]) => {
      if (value && value.length > 0) {
        if (Array.isArray(value)) {
          // Handle array filters like position, experience, industry, etc.
          if (value.length > 0 && !value.includes(job[key as keyof Job])) {
            matches = false;
          }
        } else if (key === 'minRecruiterActivity' && job.recruiterActivity < value) {
          matches = false;
        } else if (key === 'minApplicationRate' && job.applicationRate < value) {
          matches = false;
        } else if (key === 'connectionStrength' && value.length > 0) {
          if (!value.includes(job.connection.type)) {
            matches = false;
          }
        } else if (job[key as keyof Job] !== value) {
          matches = false;
        }
      }
    });
    
    return matches;
  });
};
